import type { AppState, Project, TaskPriority, TaskStatus, Todo, WorkLog, WorkLogType } from "../types";

type SampleTodoInput = {
  id: string;
  title: string;
  dueDate?: string | null;
  estimate?: string;
  status?: TaskStatus;
  progress?: number;
  workerComment?: string;
  managerComment?: string;
  issueRisk?: string;
  priority?: TaskPriority;
  memo?: string;
};

type SampleWorkLogInput = {
  id: string;
  projectId: string;
  todoId?: string;
  date: string;
  type: WorkLogType;
  content: string;
};

function createTodo({
  id,
  title,
  dueDate = null,
  estimate = "",
  status = "대기",
  progress = status === "완료" ? 1 : 0,
  workerComment = "",
  managerComment = "",
  issueRisk = "",
  priority = "보통",
  memo = "",
}: SampleTodoInput): Todo {
  const completed = status === "완료" || progress >= 1;

  return {
    id,
    title,
    dueDate,
    estimate,
    status: completed ? "완료" : status,
    progress: completed ? 1 : progress,
    workerComment,
    managerComment,
    issueRisk,
    priority,
    memo,
    completed,
  };
}

function createWorkLog(input: SampleWorkLogInput): WorkLog {
  return input;
}

